// egal
console.log(5 == 5);

// different de 
console.log(5 != 5); 

// plus grand que
console.log(5 > 5);

// plus grand ou egal que
console.log(5 >= 5);

// plus petit que
console.log(5 < 5);

// plus petit ou egal que
console.log(5 <= 5); 

// meme type et egal ( 5 et 5)
console.log(5 === 5); 

// meme type et egal ( 5 et "5")
console.log(5 === "5"); 

// type different ou inegal ( 5 et 5)
console.log(5 !== 5);

// type different ou inegal ( 5 et "5")
console.log(5 !== "5");







